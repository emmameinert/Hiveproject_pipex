#include "libft.h"

char	*ft_strdup(const char *s1)
{
	char	*cpy;
	int		i;

	cpy = (char *)malloc (sizeof(char) * ft_strlen(s1) + 1);
	if (!cpy)
		return (NULL);
	i = 0;
	while (s1[i] != '\0')
	{
		cpy[i] = s1[i];
		i++;
	}
	cpy[i] = '\0';
	return (cpy);
}
