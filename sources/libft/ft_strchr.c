char	*ft_strchr(const char *s, int c)
{
	int		i;
	char	d;

	d = (char)c;
	i = 0;
	while (s[i] != d && s[i] != '\0')
			i++;
	if (s[i] == d)
		return ((char *) &s[i]);
	return (0);
}
